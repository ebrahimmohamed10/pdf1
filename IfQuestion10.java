
package ifquestion10;


public class IfQuestion10 {

    
    public static void main(String[] args) {
        int x = 3;
        int y = 2;
        int z = x++;
        int w = ++y;
        System.out.println("x = " + x);
        System.out.println("y = " + y);
        System.out.println("z = " + z);
        System.out.println("w = " + w);
    }
    
}
